// Single source of truth for Royal Caribbean ships represented by Royal's
// official fleet/deck plan pages as of 2026-04-18.
//
// Update workflow:
// 1. Confirm the ship still appears on Royal's official deck plan index.
// 2. Update status if a ship moves from future to active.
// 3. Refresh dining arrays if Royal changes the official restaurants/bars page
//    or a ship gets amplified and picks up a materially different venue mix.
// 4. Add ship-specific notes instead of inventing new top-level fields every time.

const cloneVenues = (venues) => venues.map((item) => ({ ...item }))

const withProfile = (profileKey, overrides) => {
  const profile = diningProfiles[profileKey]

  return {
    ...overrides,
    complimentaryDining: cloneVenues(profile.complimentaryDining),
    specialtyDining: cloneVenues(profile.specialtyDining),
    hybridDining: cloneVenues(profile.hybridDining),
  }
}

